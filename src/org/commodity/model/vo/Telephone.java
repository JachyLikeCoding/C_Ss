package org.commodity.model.vo;

public class Telephone {
	private String teleno;
	private String sno;
	
	public String getTeleno()
	{
		return this.teleno;
	}
	
	public void setTeleno(String teleno)
	{
		this.teleno = teleno;
	}
	
	public String getSNo()
	{
		return this.sno;
	}
	
	public void setSNo(String sno)
	{
		this.sno = sno;
	}
}
